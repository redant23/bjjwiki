// 카테고리 키 생성 헬퍼 함수
export function getCategoryKey(mainCategory: string, subCategory?: string, detailCategory?: string): string {
  if (detailCategory && subCategory) {
    return `${mainCategory}::${subCategory}::${detailCategory}`;
  } else if (subCategory) {
    return `${mainCategory}::${subCategory}`;
  }
  return mainCategory;
}
